test('POST /api/save-env sichert bestehende .env vor dem Überschreiben', async () => {
  const dir = mkdtempSync(join(tmpdir(), 'oikos-env-'));
  try {
    writeFileSync(join(dir, '.env'), 'SESSION_SECRET=old\n');
    await withServer(dir, async base => {
      const r = await fetch(`${base}/api/save-env`, {
        method: 'POST', headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ env: { SESSION_SECRET: 'new' } }),
      });
      assert.equal(r.status, 200);
    });
    const backups = readdirSync(dir).filter(f => f.startsWith('.env.bak-'));
    assert.equal(backups.length, 1, 'genau ein .env.bak-* erwartet');
    assert.equal(readFileSync(join(dir, backups[0]), 'utf8'), 'SESSION_SECRET=old\n',
      'Backup enthält nicht den alten .env-Inhalt');
    assert.match(readFileSync(join(dir, '.env'), 'utf8'), /SESSION_SECRET=new/);
  } finally {
    rmSync(dir, { recursive: true, force: true });
  }
});

test('POST /api/save-env lehnt Newline-Werte mit 400 ab und schreibt keine .env', async () => {
  const dir = mkdtempSync(join(tmpdir(), 'oikos-env-'));
  try {
    await withServer(dir, async base => {
      const r = await fetch(`${base}/api/save-env`, {
        method: 'POST', headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ env: { SESSION_SECRET: 'a\nINJECTED=1' } }),
      });
      assert.equal(r.status, 400);
    });
    assert.equal(existsSync(join(dir, '.env')), false, 'bei Ablehnung darf keine .env entstehen');
  } finally {
    rmSync(dir, { recursive: true, force: true });
  }
});

test('GET /api/preflight liefert envExists und containerRunning', async () => {
  const dir = mkdtempSync(join(tmpdir(), 'oikos-env-'));
  try {
    await withServer(dir, async base => {
      let r = await fetch(`${base}/api/preflight`);
      assert.equal(r.status, 200);
      let d = await r.json();
      assert.equal(d.envExists, false);
      assert.equal(typeof d.containerRunning, 'boolean');

      writeFileSync(join(dir, '.env'), 'X=1\n');
      r = await fetch(`${base}/api/preflight`);
      d = await r.json();
      assert.equal(d.envExists, true);
      assert.equal(typeof d.containerRunning, 'boolean');
    });
  } finally {
    rmSync(dir, { recursive: true, force: true });
  }
});

